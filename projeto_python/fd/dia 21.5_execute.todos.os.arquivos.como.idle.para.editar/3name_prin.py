print(name.title())
#o ponto (entre name e title) serve para indicar que o title() vai se referir a variável "name".
#title() é um método que exibe cada palavra da váriavel com uma letra maiuscula no inicio.
name = "Ada lovelace"
print(name.upper())
print(name.lower())
#upper() deixa todas as letras maisculas, lower() todas minusculas.
